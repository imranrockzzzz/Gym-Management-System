    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
       int selectedRow=jTable1.getSelectedRow();
        TableModel model= jTable1.getModel();
        String name=model.getValueAt(selectedRow, 1).toString();
        String quantity=model.getValueAt(selectedRow, 2).toString();
        String puc=model.getValueAt(selectedRow, 3).toString();
        String totalcost=model.getValueAt(selectedRow, 4).toString();
        String id=model.getValueAt(selectedRow, 0).toString();
        a.setVisible(true);
        a.pack();
        a.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        a.jTextNameUpdate.setText(name);
        a.jTextQuantityUpdate.setText(quantity);
        a.jTextPUCUpdate.setText(puc);
        a.jTextTotalCostUpdate.setText(totalcost);
        a.jTextEquipID.setText(id);
    }//GEN-LAST:event_jBtnUpdateActionPerformed

